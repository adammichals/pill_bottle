void HAL_TIM_OC_DelayElapsedCallback(TIM_HandleTypeDef *htim)
{
	// timer for motor stepping
	if (htim == &htim1)
	{
		// act only on rising edge of step
		if (tim_state == 0)
		{
			motor_count++;
			tim_state = 1;
			//HAL_GPIO_WritePin(LD4_GPIO_Port, LD4_Pin, GPIO_PIN_SET);
		}
		else
		{
			tim_state = 0;
			//HAL_GPIO_WritePin(LD4_GPIO_Port, LD4_Pin, GPIO_PIN_RESET);

			// check for end of movement
			if (motor_count >= set_motor_steps)
			{
				// stop timer
				(htim->Instance)->CR1 &= (!0x0001);

				// reset control variables
				(htim->Instance)->CNT = 0;
				motor_count = 0;
			}
		}
	}

	// timer for dosage/ADC checking
	else
	{
		HAL_GPIO_TogglePin(LD4_GPIO_Port, LD4_Pin);

		// count down dosage timer
		if (dosageTimer > 0)
		{
			dosageTimer--;
			if (dosageTimer == 0)
			{
				speakerOn = 1;
				TIM15->CR1 |= 0x0001;
			}
		}
		else
		{
			if (speakerOn == 1)
			{
				speakerOn = 0;
				TIM15->CR1 &= (!0x0001);
			}

		}



		// ADC check
		HAL_ADC_Start(&hadc);
		HAL_ADC_PollForConversion(&hadc, 10000);
		uint32_t value = HAL_ADC_GetValue(&hadc);
		HAL_ADC_Stop(&hadc);

		// unlock if ADC input is too low
		if (value <= 154)
		{
			HAL_GPIO_WritePin(LOCK_GPIO_Port, LOCK_Pin, GPIO_PIN_SET);
		}
		else
		{
			HAL_GPIO_WritePin(LOCK_GPIO_Port, LOCK_Pin, GPIO_PIN_RESET);
		}
	}
}

